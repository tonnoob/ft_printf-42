#include "ft_printf.h"

int	ft_print_nbr(long nb)
{
	int		i;

	i = 0;
	ft_putnbr_fd(nb, 1);
	if (nb < 0)
	{
		nb *= -1;
		i++;
	}
	while (nb >= 10)
	{
		nb = nb / 10;
		i++;
	}
	i++;
	return (i);
}
